				<h1>The Forge Fire</h1>
				<p>The forge fire is the heart of the blacksmith&#39;s shop. It is where all work begins, and were a fair amount of it ends. A well built and maintained forge fire will supply all the heat you need to shape iron and steel into any shape you would like. A bad fire will give you nothing but trouble.
			</div><!--l-1-->
			
		
		<div class="col-2 left">
			<div id="l-2" class="col-2 left">
				<h2>Building The Fire</h2>
				<p>Before you can forge iron first you
				need to build a fire.
				</p>

				<p><b>Step #1:</b> Thoroughly clean out your fire
				box. Removing all debris form the last fire.
				</p>

				<p><b>Step #2:</b> Once the firebox is clean, make a ring
				of fresh coal around the air grate(tuyere) of your forge. Place a few
				large lumps of coal on top of the grate.
				</p>

				<p><b>Step #3:</b> Crumple newspaper on top of the lumps
				in the center of the ring.
				</p>

				<p><b>Step #4:</b> Put some kindling wood on top of the
				paper and light the paper.
				</p>

				<p><b>Step #5:</b> Once the paper is burning, slowly turn
				on the air supply until the kindling lights.
				</p>

				<p><b>Step #6:</b> Once the wood is burning well add some
				additional green (unburned) coal to the fire. Slowly turn up the air
				until the coal ignites. You can tell when the coal has started by
				appearance of yellow smoke, and a sweet, pleasant smell. 
				</p>

				<p><b>Step #7:</b> Add more green coal until the fire is
				big as you need it to be.
				</p>

				<p><b>Step #8:</b> While the fire is developing,
				occasionally sprinkle it with a little bit of water to help remove
				the impurities from the fuel. Be careful not to use to much water or
				the fire could go out. Once the fire is going you can dampen the coal
				before adding it to the fire.
				</p>
			</div><!--l-2--> 
			
			<div id="l-3" class="col-2 left">
				<h2>Tending the fire. </h2>

				<p>You will need to break up the coal in
				the forge because it has a tendency to congeal into a solid mass
				while burning. Turn the Fire over in the forge and break it up into
				thumb sized pieces.
				</p>

				<p>As the impurities burn out the green
				coal reduces down into coke. This is when the fire burns at it's
				best, but as the fire continues to burn it will slowly get dirtier
				and go out. To keep the fire burning you must add more coal and
				remove 'clinkers'.
				</p>

				<p>Clinkers are dark, glassy lumps of
				residue that form it the fire while forging. They are be extremely
				irritating because they stick to hot metal. Clinkers can not be
				prevented, so they must be properly dealt with.
				</p>

				<p>Clinkers form at the bottom of the fire
				around the air grate. If clinkers build up they can clog the air
				supply. The clinkers must be removed as soon as you suspect they have
				formed. If the air blast has been cut down or glassy globs are stuck
				on the metal clean the fire.
				</p>

				<p>You can tell a clinker by it's glossy
				luster and darker color. Remove them with the poker or fire tongs. If
				left in clinkers will get imbedded into the surface of your work,
				cause welds to fail, and just make things nasty in general. With a
				little practice removing clinkers becomes easy.
				</p>

				<p>Add wet coal around the edges of the
				fire away from the working area. As the coal burns clean rake it into
				the working area. Do not add green coal directly into the work area,
				this will dump all the impurities from the green coal into the fire
				you just burned clean.
				</p>
			</div><!--l-3-->
			
			<div id="l-4" class="col-2 left">
				<h2>Putting Out The Fire</h2>
				
				<p><b>WARNING:</b>Never dump water directly into a forge
				fire unless there is an emergency. Doing so will cause the grate or
				possibly the whole fire box to crack or shatter, create lots of
				scalding steam, short out any electricity, and make refractory
				material explode.
				</p>

				<p>To properly put out a forge fire, turn
				off the air, open the ash dump (to prevent any gas build up), and
				clear away any flammable items. The fire will burn down in a few
				minutes. It will still be hot enough to burn, so don't touch it.
				Leave it alone and it will extinguish itself.
				</p>

				<p>After the the forge is completely shut
				down, Shovel the coal and ash into a water filled trash can, stir it
				well to be sure that all the hot material is extinguished.
				</p>
			</div><!--l-4-->
