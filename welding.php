		<div class="col-2 left">
			<div id="l-2" class="col-2 left">
				<h2>Flux</h2>				
				<p>Welding requires flux. Flux Cleans the meeting surfaces and allows them to run together. The flux combines with the scale so that it melts and then prevents further scaling or oxidization. There are various formulas for making your own welding flux, most are based on borax. Until you get some practice welding it will probably be easier to just buy commercially made flux. That way you know any problems are not caused by the flux.</p>				
			</div><!--l-2--> 
			
			<div id="l-3" class="col-2 left">
				<h2>Scarfing</h2>
				<p>Ends to be joined have to be prepared by scarfing. They are upset and hammered to thick tapered ends.</p>
				<p id="w1" class="pict"><img src="images/w1.gif" alt="." /></p>
				<p>The surfaces to meet are given round shapes.</p>
				<p id="w2" class="pict"><img src="images/w2.gif" alt="." /></p>
				<p>This is so that their centers meet and force out flux an scales. If the meeting centers flow together during the first hammer blows, the weld should be satisfactory.</p>
			</div><!--l-3-->
			
			<div id="l-4" class="col-2 left">
				<h2>Heating</h2>
				<p>Heat the ends that have been prepared by scarfing. When they have reached an orange heat, pull them from the fire and sprinkle flux on them. Return the ends to the fire, and make sure they are in the center where the heat will be greatest. When the steel glows a light yellow, turn the faces downward. Continue heating the steel. A few sparks coming from the fire will indicate the welding temperature has been reached.</p>
			</div><!--l-4--><!---->
			
			<div id="l-5" class="col-2 left">
				<h2>Hammering</h2>
				<p>Have the anvil face clean and the Hammer ready. Have a helper hold one piece with the scarf upwards, and you position the other over it  and hammer the parts together immediately. The cold anvil face will take heat away from the weld. This can be reduced by bringing the parts together at a slight angle.The first blow brings the join to the anvil and also makes the weld. </p>
				<p id="w3" class="pict"><img src="images/w3.gif" alt="." /></p>
				<p>Hammering has to be done systematically. The first few blows are in the center, then towards the ends of the scarfs to close the thin parts. More blows come at the edges, and the work can be turned on it&#39;s side to get the width of the weld. It might be necessary to reheat if the weld is not fully closed before it cools. Clean the area with a wire brush before returning it to the fire. Bring it to welding heat again and quickly hammer the parts of the weld that still need treatment.</p>
