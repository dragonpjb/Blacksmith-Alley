				<h2>A Shop Apron</h2>
				<p>A shop apron is important to protect you from the fire and hot metal. It should cover you from chest to knees, and be made of thick leather that sheds sparks. Heavy cotton or wool will do if nothing else is available, but leather is the best choice. It is a little more expensive, but you only have to buy it once. </p>
			</div><!---->
			
