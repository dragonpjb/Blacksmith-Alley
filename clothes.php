				<h2>Shoes and Socks</h2>
				<p>Closed toed shoes should always be worn in the shop. Sneakers will do but, ideally they should be sturdy work boots with high tops covered by your pants legs. Good shoes protect your feet from falling objects, burns, punctures and cuts from sharp things on the floor, and accidentally kicking things.</p>
