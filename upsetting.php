				<h2>Upsetting Smaller Pieces</h2>
				<p>For a shorter piece of iron it is best to hold it on the anvil and strike the cold end with a hammer. Several reheat will probably be needed to get a worth while amount of upsetting.</p>
				<p id="u2" class="pict"><img src="images/u2.gif" alt="." width="350"/></p>
