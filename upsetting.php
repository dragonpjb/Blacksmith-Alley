				<h2>Backing Up</h2>
				<p>In most cases, upsetting is best started by jumping up. Once spreading an shortening has started, the process can be continued with backing up blows from the hammer. Have the end at a good heat. Hold it so that it projects over the edge of the anvil, with  your holding hand braced against your thigh , ready to resist the shock of hammering. Hit the end strongly while rotating the rod between blows so as to get an even effect.</p>
