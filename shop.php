                <h2>Lighting</h2>
                <p>The right lighting is important. The forge area should be dim (but not dark) to allow for proper judgment of metal color. The rest of the shop should be brightly lit so you can see what you are doing, especially any areas where power tools like grinders are used. The best lighting is natural background light, but this is not always available. The next best thing is overhead fluorescent light. Any lighting that allows you to see clearly with out eyestrain is acceptable.</p>
