                <p>When laying out your shop pay attention to how you arrange your equipment. Minimize extra work by making sure you do not have to go out of your way to reach any thing you need. Your forge, anvil, and vise should be close together to minimize heat loss. Your slack tank should be near by so you do not have to carry hot metal across your shop. Grinder and drill press should be close to your work bench.</p>
