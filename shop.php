                <p>The most important considerations in setting up a shop is adequate ventilation. With out sufficient air flow you will have some serious and potentially deadly problems. Forges use vast amounts of air, and produce large amounts of carbon monoxide(CO). Carbon monoxide is a colorless, odorless, and highly toxic. In an inclosed area it can quickly reach fatal levels. </p>
            </div>
        
