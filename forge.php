				<h2>The Relative Importance Of Things</h2>
				<p>The blacksmith could operate without many of his other specialized tools if they were not available, but not a good forge to heat the metal. Let&#39;s examine the relative importance of each major tool in the smithy individually
				</p>
			</div><!--l-1-->
			
			<div id="l-2" class="col-2 left">		
				<h3>Do you lack an anvil?</h3>
				<p>As important as the anvil is, a good flat rock will do in a pinch. That's probably what the first metal workers used any way, and they got by just fine. For some things a flat granite slab is better than a metal anvil any way, and good granite slabs can easily be acquired at stone shops that manufacture tombstones for little to nothing. They will usually sell or give away their left over bits of stone just to get rid of them.
				</p>
			</div><!--l-2-->
			
		
			<div id="l-3" class="col-2 left">		
				<h3>Do you Lack a Hammer?</h3>
				<p>No hammer? A smooth rock on a stick could be substituted. Even the stick is really optional. Just the rock would do the job, although without the leverage afforded by the handle. If  the rock was large and smooth enough to comfortably hold in your hand and protect it from the fire, it would do the job and it would be free.
				</p>
			</div><!--l-3-->
			
			<div id="l-4" class="col-2 left">		
				<h3>Do you lack tongs?</h3>
				<p>No Tongs? Make the work piece long enough so that one end stays cool while the other end is in the fire and the tongs are not needed. If leaving extra metal on work piece is not practical,forge weld it to the end of a long piece scrap iron that is lying around, and cut it off when it is done. This is what Japanese smiths normally do when making katanas, they feel it gives them better control. Like the rock hammer, the length of scrap iron is also essentially free.
				</p>
			</div><!--l-4-->
			
			<div id="l-5" class="col-2 left">		
				<h3>Do you lack an air supply?</h3>
				<p>No bellows or electric blower for the fire? A long blow pipe works fairly well. Some smiths in Africa and rural Asia still do it this way. It does not even slow them down. The blow pipe also does not require maintenance or an outside power source. It can also be acquired very cheaply, if not free.
				</p>
			</div><!--l-5-->
			
			<div id="l-6" class="col-2 left">		
				<p>We have now assembled a basic blacksmiths shop from a couple of rocks and pieces of scrap metal that you could probably find on a curb if you look carefully enough. Mind you, I am not saying it is necessarily a good idea, just that it is possible and inexpensive. However, without a good forge fire to heat the metal in, none of the rest of it matters.
				</p>
