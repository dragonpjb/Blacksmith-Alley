			<h1>The Forge</h1>
			
			<p>The forge is where most of the operations in a blacksmith's  shop begin.  Before shaping a piece of metal, it must be heated to working temperature in the fires of the forge. The shaping process usually consists of repeated heating of the metal as the hammering process hardens it. After the forming process the metal is very hard and brittle from the hammering, and it must be brought back to the hardness and flexility required by the object the metal is being used for. Because the forge is so central to all of the blacksmithing processes the shop should be arranged around it.
			</p>
		</div><!--title-->
		
