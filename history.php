				<h1>A Short History of Blacksmithing</h1>
				
				<p>
					No one knows exactly where and when the first blacksmith shaped a piece of metal. 
					The development of blacksmithing was so important that the  various ages of human 
					advancement are named after the materials used by them. The Stone Age, The Copper 
					Age, The Bronze age and the Iron Age mark man&#39;s growth from the primitive 
					cave dweller to our modern society.
				</p>
