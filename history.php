			<div class="col-2 left">
				<p>
					Although, working with stone would not now be considered smithing it held the 
					same place in society as that of metal working which came later. There is 
					evidence that copper was in use as early as 9000 BC. in the Middle East. When 
					ever it happened, it was probably the same person who previously had been 
					chipping flint into tools and weapons. The discovery of copper began mankind&#39;s 
					association with metal and the development of blacksmithing. The development 
					of blacksmithing and that of human culture have been co-dependent from the 
					moment the first piece of copper was picked up off the ground. This first 
					piece was probably used as jewelry.
				</p>
