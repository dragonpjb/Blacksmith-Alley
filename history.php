			<div class="col-2 left">
				<h2>Copper</h2>
				
				<p>Smithing was at first just the shaping of the pieces of copper that were dug from the ground. It probably involved a stone hammer and a stone anvil. When it was discovered that the pretty green streaks in the rocks and soil around the copper were also copper the smelting process began. It might have happened when copper melted out of rocks used to bank a fire or when someone was trying to get a piece of pure copper out of a chunk of ore; we&#39;ll never know. Such momentous discoveries are often the result a simple accident. This is estimated to have happened around 4500 BC. An Iron Age smelting site, Agia Varvara-Almyras, in Cyprus, had its complete operations recorded in written documents, which has contributed to much of our knowledge of ancient techniques. (CSA.Com).</p>

				<p>The ancient Egyptians  represented copper with the AnkhIt symbol and used the metal to shape their temples and pyramids. Copper pipes have been found in tombs dating from around 4000 BC. Blacksmith shops have been found in the ruins of the towns built for the workers enlisted by the Egyptians.  The word cuprum evolved from the country Cyprus. It has a melting point of 1981.4<sup>o</sup> F and boils at 4652.6<sup>o</sup> F. (CSA. Com). </p>

				<p>The tempering and malleability of copper happened naturally has the metal was processes for tools. We don&#39;t know when copper processing was discovered in China but it was in common use as far back as 3000 years ago. We know that the Egyptians began combining zinc with copper to make bronze which is harder than copper alone. This process ushered in the Bronze Age. </p>
			</div>
			
			<div class="col-2 left">
				<h2>Discovering Iron</h2>
				<p>Today, blacksmithing is most commonly associated with the working of iron and steel. The discovery of iron was probably made at about the same time as that of copper but the melting point is so much higher delayed  it&#39;s use until  a method of melting it was developed. The production of iron by humans began probably sometime after 2000 BC in south-west or south-central Asia, perhaps in the Caucasus region. (Anselm.edu)</p>

				<p>The Chalybes, a Scythian tribe living south and east of the Black Sea, are accredited by ancient writers with being the first to use coal in their furnaces. The first forges were simply coal fires combined with a blow pipe to increase the heat. </p>

				<p>The Egyptians and the Assyrians made iron very early and iron utensils and weapons have been found in recently unearthed tombs. The ancient Hittites conquered much of their world because they were the first to use iron weapons. (oldandsold.com) </p>

				<p>There is evidence that weapon makers were so important at the time that they were usually immune from harm by conquering armies. The books of Moses mention the use of iron some eleven centuries before the Christian era, and the Arundelian marbles fix a date for it before 1370 BC.(oldandsold.com) The production of iron by humans began probably sometime after 2000 BC in south-west or south-central Asia, perhaps in the Caucasus region.</p>
			</div>
			
			<div class="col-2 left">
				<h2>Blasting Wrought Iron</h2>
				<p>We most often associate blacksmithing with the working of wrought iron and steel. When limestone or sea shells are mixed in and, the iron ore is then heated in a charcoal fire, the iron ore (Iron oxide) begins to release some of its oxygen, to produce a spongy, porous mass of relatively pure iron mixed with bits of charcoal and impurities in the ore called slag. The early blacksmith would hammer these impurities from the hammered or worked <I>wrought iron</I>; the most commonly produced metal of the Iron Age.</p>

				<p>By the late Middle Ages, European ironmakers had developed the blast furnace; a tall, round structure that pumps a blast of air through the ore and charcoal intensifying the heat. At the high temperatures possible with it, great quantities of iron could be produced in a pure form. </p>
			</div>
			
			<div class="col-2 left">
				<h2>Puddling Pig Iron</h2>
				
				<p>With the invention the puddling furnace which keeps the iron separate from the charcoal and stirs it to remove the impurities. In the middle ages in Europe, iron could be produced in greater quantities. As the iron was released from the furnace it would follow a channel of sand troughs which in-turn emptied into smaller troughs which looked to the iron workers like a mother pig suckling her piglets; hence, the term pig iron. This iron was too hard and brittle to work with a hammer so it was used for casting. Iron castings of stoves, cannons, cannon balls and flat irons etc.. </p>

				<p>After 1784, pig iron was additionally refined in a <I>puddling furnace </I>(developed by the Englishman Henry Cort) that kept the molten iron separate from the charcoal and stirred it. The bits of iron produced would then be hammered into a single piece of wrought iron. The production of wrought iron was limited by the fact that the bits of iron produced in the process had to be pulled from the mixture by specialists called <I>Puddlers </I>and could not be mechanized.</p>

				<p>In the 1700s an Englishman named Abraham Darby discovered coke. Coke is made by baking coal to remove impurities and could be substituted for charcoal, the production of which was deforesting Europe. <I>Coke</I> is a contraction of coal cake. </p>
			</div>
			
			<div class="col-2 left">
				<h2>Creating Steel</h2>
				
				<p>Steel is between .2 to 1.5 percent carbon placing it midway between cast iron and wrought iron in carbon content. This makes it harder than wrought iron, yet malleable and flexible, unlike cast iron.  This hardness and flexibility of steel makes it superior to the other types of iron allowing the production sharper and more shock and tension resistant tools and structures. Although steel was made prior to the mid 1800s it was difficult and expensive to manufacture. </p>
			</div>
			
			<div class="col-2 left">
				<h2>The Bessemer Process</h2>
				
				<p>In 1856, British metallurgist Sir Henry Bessemer (1813-1898) designed what he called a <I>converter</I>, which allowed the injection of compressed air which very quickly removed all of the impurities in pig iron. Later, another British inventor, Robert Mushet, showed that the air blast actually removed too much carbon and left too much oxygen behind in the molten metal and that the addition of a compound of iron, carbon, and manganese called <I>spiegeleisen,</I> would remove the oxygen in the form of manganese oxide, which passes into the slag, and the carbon remains behind, converting the molten iron into steel.</p>

				<p>While vastly improving production, it did not remove phosphorus from iron which made the steel produced too brittle. This limited the use of the Bessemer process to the rare phosphorus free ore. In 1876, Sidney Gilchrist Thomas discovered that adding limestone to the converter draws the phosphorus from the pig iron resulting in phosphorus-free steel. This Thomas Basic process meant that iron ore from all over the world could be used to make steel. Subsequent refinements to the making of steel have improved either production or quality or, in some, cases both.</p>
			</div>
			
			<div class="col-2 left">
				<h2>Emerging Specialties</h2>
				
				<p>As this whole iron industry evolved over time, the village black smith who was a generalist that did a little of everything, evolved into specialists who mastered specific skill sets. The Armorer, the Bladesmith, the locksmith, the gunsmith, and the Farrier  that shoes horses, were all offshoots from the smithy. The smithy made the tools and implements of Colonial America including axes, knifes, door hinges or a handful of nails. He repaired chains, wagon wheel rims and any other broken iron implements. The blacksmith was necessary for the colonists to survive.  Even though blacksmith were common in the colonies of North America, iron and steel had to be brought from abroad until the ore deposits of Mid America were discovered.</p>

				<p>The development of the many tools and their individual requirements led to the invention of the different tool steels and alloys we have today. Tools had to be of different hardness and workability which required constant tinkering with ways to improve iron.</p>
			</div>
			
			<div class="col-2 left">
				<h2>The End</h2>
				
				<p>The Industrial Age brought an end to blacksmithing the tools of America. Soon after the Civil War, hardware and farm equipment was being manufactured in large plants and distributed by hardware stores. After the automobile was invented wagon makers and harness makers declined and died out.</p>

				<p>The blacksmith survived but only as a specialty, but most of the rest were relegated to the maintenance departments in the plants that replaced them. Blacksmithing continued producing intricate and ornate works used for staircases and fences. The Great Depression ended that specialty. </p>
			</div>
			
			<div class="col-2 left">
				<h2>A New Beginning</h2>
				
				<p class="col-2">Only in the last 40 years has blacksmithing made a comeback. The art today is different, however. It exists as hobbyists at theme parks, restoration villages, craft fairs and craft shops. Blacksmith recreate the past but also have developed into an industry making fine knives which are in high demand; knives that continue to create new processes as well as keeping the old ones alive.</p>
